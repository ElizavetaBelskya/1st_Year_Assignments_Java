
public class ExpandableArrayIndexOutOfBoundsException extends RuntimeException {
    public ExpandableArrayIndexOutOfBoundsException() {
    }

    public ExpandableArrayIndexOutOfBoundsException(String message) {
        super(message);
    }

    public ExpandableArrayIndexOutOfBoundsException(String message, Throwable cause) {
        super(message, cause);
    }

    public ExpandableArrayIndexOutOfBoundsException(Throwable cause) {
        super(cause);
    }

    public ExpandableArrayIndexOutOfBoundsException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
